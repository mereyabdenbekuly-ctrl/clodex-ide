import { createHash } from 'node:crypto';
import { existsSync, lstatSync, readFileSync, readdirSync } from 'node:fs';
import { basename, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const policyFilename = '.clodex-boundary-policy.json';
const ignoredDirectories = new Set([
  '.git',
  '.turbo',
  'build',
  'coverage',
  'dist',
  'node_modules',
  'out',
  'vendor',
]);
const sourceExtensions = new Set([
  '.cjs',
  '.cts',
  '.js',
  '.jsx',
  '.json',
  '.mjs',
  '.mts',
  '.ts',
  '.tsx',
  '.yaml',
  '.yml',
]);
const dependencyFields = [
  'dependencies',
  'devDependencies',
  'optionalDependencies',
  'peerDependencies',
];
const forbiddenDependencyProtocols =
  /^(?:(?:workspace|file|link|portal|patch|catalog|ssh|git|github|gitlab|bitbucket|https?):|git\+|\.{1,2}[/\\]|[/\\]|[^@\s]+@[^:]+:)/iu;
const generatedMarker = /(?:@generated\b|generated by\b|do not edit\b)/iu;
const restrictedPathMarkers = [
  'CLODEx_Private_Data_Room',
  '.codex/attachments',
  'PITCH_DECK',
  'investor',
  'customer-data',
  'production-topology',
];
const secretPatterns = [
  {
    label: 'private key material',
    pattern: /-----BEGIN (?:RSA |EC |OPENSSH )?PRIVATE KEY-----/u,
  },
  {
    label: 'GitHub personal access token',
    pattern: /\bghp_[A-Za-z0-9]{20,}\b/u,
  },
  {
    label: 'OpenAI-style secret key',
    pattern: /\bsk-[A-Za-z0-9_-]{20,}\b/u,
  },
  {
    label: 'AWS access key',
    pattern: /\bAKIA[0-9A-Z]{16}\b/u,
  },
  {
    label: 'Slack bot token',
    pattern: /\bxoxb-[0-9A-Za-z-]{20,}\b/u,
  },
];

function extensionOf(path) {
  const name = basename(path);
  const index = name.lastIndexOf('.');
  return index === -1 ? '' : name.slice(index);
}

function parseJson(path, label, errors) {
  try {
    return JSON.parse(readFileSync(path, 'utf8'));
  } catch (error) {
    errors.push(label + ': invalid JSON: ' + error.message);
    return null;
  }
}

function sha256(path) {
  return createHash('sha256').update(readFileSync(path)).digest('hex');
}

function walk(directory, root, errors) {
  const files = [];
  for (const entry of readdirSync(directory)) {
    const path = join(directory, entry);
    const stat = lstatSync(path);
    const name = relative(root, path).replaceAll('\\', '/');
    if (stat.isSymbolicLink()) {
      errors.push(name + ': symlinks are forbidden in the private boundary');
      continue;
    }
    if (stat.isDirectory()) {
      if (!ignoredDirectories.has(entry)) {
        files.push(...walk(path, root, errors));
      }
    } else {
      files.push(path);
    }
  }
  return files;
}

function packageAllowed(name, allowed) {
  return allowed.some(
    (candidate) => name === candidate || name.startsWith(candidate + '/'),
  );
}

function forbiddenPackage(name, allowed) {
  if (packageAllowed(name, allowed)) return false;
  return name.startsWith('@stagewise/') || name.startsWith('@clodex/');
}

function checkDependency(
  manifestName,
  field,
  dependency,
  specifier,
  allowed,
  errors,
) {
  const location = manifestName + ':' + field + '.' + dependency;
  if (forbiddenPackage(dependency, allowed)) {
    errors.push(location + ': forbidden implementation package');
  }
  if (typeof specifier !== 'string' || specifier.length === 0) {
    errors.push(location + ': dependency specifier must be a non-empty string');
    return;
  }
  if (forbiddenDependencyProtocols.test(specifier)) {
    errors.push(
      location + ': local, URL, Git, workspace, or patch source forbidden',
    );
  }
  if (
    specifier.startsWith('npm:') &&
    (specifier.includes('@stagewise/') || specifier.includes('@clodex/'))
  ) {
    const approvedAlias = allowed.some((candidate) =>
      specifier.startsWith('npm:' + candidate + '@'),
    );
    if (!approvedAlias) {
      errors.push(
        location + ': alias targets a forbidden implementation package',
      );
    }
  }
}

function visitOverrideStrings(value, label, manifestName, allowed, errors) {
  if (typeof value === 'string') {
    checkDependency(manifestName, label, label, value, allowed, errors);
    return;
  }
  if (!value || typeof value !== 'object') return;
  for (const [key, child] of Object.entries(value)) {
    visitOverrideStrings(
      child,
      label + '.' + key,
      manifestName,
      allowed,
      errors,
    );
  }
}

function checkManifest(path, root, allowed, errors) {
  const name = relative(root, path).replaceAll('\\', '/');
  const manifest = parseJson(path, name, errors);
  if (!manifest) return;
  for (const field of dependencyFields) {
    for (const [dependency, specifier] of Object.entries(
      manifest[field] ?? {},
    )) {
      checkDependency(name, field, dependency, specifier, allowed, errors);
    }
  }
  for (const [label, value] of [
    ['overrides', manifest.overrides],
    ['resolutions', manifest.resolutions],
    ['pnpm.overrides', manifest.pnpm?.overrides],
    ['pnpm.patchedDependencies', manifest.pnpm?.patchedDependencies],
  ]) {
    if (value) visitOverrideStrings(value, label, name, allowed, errors);
  }
  for (const script of Object.values(manifest.scripts ?? {})) {
    if (
      typeof script === 'string' &&
      /(?:\bcurl\b|\bwget\b).*(?:github|clodex-ide)|(?:file|link|workspace):/iu.test(
        script,
      )
    ) {
      errors.push(
        name + ': lifecycle script fetches or links unreviewed source',
      );
    }
  }
}

function checkSource(
  path,
  root,
  policy,
  approvedGenerated,
  forbiddenDigests,
  errors,
) {
  const name = relative(root, path).replaceAll('\\', '/');
  const source = readFileSync(path, 'utf8');

  for (const marker of restrictedPathMarkers) {
    if (name.includes(marker) || source.includes(marker)) {
      errors.push(name + ': restricted/private material marker ' + marker);
    }
  }
  for (const secret of secretPatterns) {
    if (secret.pattern.test(source)) {
      errors.push(name + ': possible ' + secret.label);
    }
  }

  for (const match of source.matchAll(
    /['"](@(?:stagewise|clodex)\/[^'"]+)['"]/gu,
  )) {
    if (forbiddenPackage(match[1], policy.allowedPublishedPackages)) {
      errors.push(name + ': forbidden source dependency ' + match[1]);
    }
  }

  const fileDigest = sha256(path);
  const forbidden = forbiddenDigests.get(fileDigest);
  if (forbidden) {
    errors.push(name + ': exact copied-source fingerprint from ' + forbidden);
  }

  if (generatedMarker.test(source.slice(0, 2048))) {
    const approval = approvedGenerated.get(name);
    if (!approval || approval.sha256 !== fileDigest) {
      errors.push(name + ': generated input is unreviewed or hash-drifted');
    }
  }
}

function validatePolicy(policy, errors) {
  if (
    policy?.schemaVersion !== 1 ||
    policy?.status !== 'REFERENCE_POLICY_UNAPPROVED' ||
    policy?.gatewayImplementationAuthorized !== false
  ) {
    errors.push(policyFilename + ': invalid reference-only policy state');
  }
  if (!Array.isArray(policy?.allowedPublishedPackages)) {
    errors.push(policyFilename + ': allowedPublishedPackages must be an array');
  }
  if ((policy?.allowedPublishedPackages ?? []).length !== 0) {
    errors.push(
      policyFilename +
        ': no published Protocol v0 or SDK package is approved yet',
    );
  }
  if (!Array.isArray(policy?.approvedGeneratedInputs)) {
    errors.push(policyFilename + ': approvedGeneratedInputs must be an array');
  }
  if (!Array.isArray(policy?.forbiddenFileSha256)) {
    errors.push(policyFilename + ': forbiddenFileSha256 must be an array');
  }
}

export function checkPrivateDependencyFirewall(
  root,
  policyPath = join(root, policyFilename),
) {
  const errors = [];
  if (!existsSync(policyPath)) {
    return [relative(root, policyPath) + ': boundary policy is required'];
  }
  const policy = parseJson(policyPath, policyFilename, errors);
  if (!policy) return errors;
  validatePolicy(policy, errors);

  const files = walk(root, root, errors);
  const allowed = policy.allowedPublishedPackages ?? [];
  for (const path of files.filter(
    (file) => basename(file) === 'package.json',
  )) {
    checkManifest(path, root, allowed, errors);
  }

  const approvedGenerated = new Map(
    (policy.approvedGeneratedInputs ?? []).map((entry) => [entry.path, entry]),
  );
  const forbiddenDigests = new Map(
    (policy.forbiddenFileSha256 ?? []).map((entry) => [
      entry.sha256,
      entry.source,
    ]),
  );
  for (const path of files.filter((file) =>
    sourceExtensions.has(extensionOf(file)),
  )) {
    if (resolve(path) === resolve(policyPath)) continue;
    checkSource(
      path,
      root,
      policy,
      approvedGenerated,
      forbiddenDigests,
      errors,
    );
  }
  return errors;
}

function argument(name) {
  const prefix = '--' + name + '=';
  const value = process.argv.find((entry) => entry.startsWith(prefix));
  return value ? value.slice(prefix.length) : null;
}

function main() {
  const root = resolve(argument('root') ?? process.cwd());
  const policy = argument('policy');
  const policyPath = policy ? resolve(policy) : join(root, policyFilename);
  const errors = checkPrivateDependencyFirewall(root, policyPath);
  if (errors.length > 0) {
    for (const error of errors) {
      console.error('private-boundary-firewall: ' + error);
    }
    process.exitCode = 1;
  } else {
    console.log(
      'Private dependency firewall: PASS (reference policy only; Gateway remains unauthorized)',
    );
  }
}

if (process.argv[1] === fileURLToPath(import.meta.url)) main();
